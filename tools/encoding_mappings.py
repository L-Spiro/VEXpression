def get_windows_id(name):
    """
    Maps a string to a Windows Code Page ID.
    Returns None if no Windows ID is found.
    """
    n = name.upper().replace("_", "-")
    
    # --- A. Explicit Mapping for Standards ---
    # These are the 'Big Ones' that don't always have the ID in the name
    standards = {
        "UTF-8": 65001,
        "UTF8": 65001,
        "65001": 65001,
        "US-ASCII": 20127,
        "ASCII": 20127,
        "UTF-16": 1200,
        "UTF-16LE": 1200,
        "UTF-16BE": 1201,
        "UTF-32": 12000,
        "UTF-7": 65000,
    }
    if n in standards:
        return standards[n]

    # --- B. Handle ISO-8859 Series ---
    # ISO 8859-1 is 28591, 8859-2 is 28592, etc.
    iso_match = re.search(r'ISO-?8859-?(\d+)', n)
    if iso_match:
        return 28590 + int(iso_match.group(1))

    # --- C. Extract from CP/Windows/IBM/MS Prefixes ---
    # Matches: CP1252, Windows-1252, IBM850, MS-ANSI (1252)
    if n == "MS-ANSI" or n == "ANSI": return 1252
    
    # Generic digit extraction for things like CP1252 or Windows1251
    digit_match = re.search(r'(?:CP|WINDOWS|IBM|MS|DOS|WIN)-?(\d+)', n)
    if digit_match:
        return int(digit_match.group(1))
        
    # Final fallback: If the string IS just a number, it's likely the ID
    if n.isdigit():
        return int(n)

    return None

def process_windows_encodings(names):
    assigned_ids = set()
    final_table = []

    for original_name in names:
        win_id = get_windows_id(original_name)

        # Rule #3: Reject "Unknown" entries
        if win_id is not None:
            # Rule #1: No duplicates. The first string for this ID stays.
            if win_id not in assigned_ids:
                final_table.append((win_id, original_name))
                assigned_ids.add(win_id)

    # Rule #2: Sort by Windows ID
    final_table.sort(key=lambda x: x[0])
    return final_table

def to_identifier(name):
    # 1. Convert to uppercase
    # 2. Replace any sequence of non-alphanumeric chars with a single underscore
    ident = re.sub(r'[^A-Z0-9]+', '_', name.upper())
    # 3. Strip leading/trailing underscores
    ident = ident.strip('_')
    # 4. If it starts with a digit, prefix with an underscore
    if ident[0].isdigit():
        ident = "_" + ident
    return ident

